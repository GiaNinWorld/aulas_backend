package src;

public class repeticao {
    public static void main(String[] args) {
        for(int i = 0; i < 5; i++) {
            System.out.println("Iteração: " + i);
        }

        int ii = 0;
        while(ii < 5){
            System.out.println("Iteração While: " + ii);
            ii++;
        }
    }
}
